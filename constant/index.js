export const fetchWeatherImages = async (query = 'weather') => {
  try {
    const apiKey = import.meta.env.VITE_PEXELS_API_KEY;
    if (!apiKey) {
      console.warn('VITE_PEXELS_API_KEY not set');
      return [];
    }

    // Pexels API allows max per_page=80, and max page=100 (so 8000 results)
    const perPage = 80;
    const maxPage = 100;
    const randomPage = Math.floor(Math.random() * maxPage) + 1;

    const res = await fetch(`https://api.pexels.com/v1/search?query=${encodeURIComponent(query)}&per_page=${perPage}&page=${randomPage}`,
      {
        headers: {
          Authorization: apiKey,
        },
      }
    );

    if (!res.ok) throw new Error('Pexels API failed');
    const data = await res.json();
    
    if (data.photos && data.photos.length > 0) {
      // Pick up to 5 random unique images from this page
      const shuffled = data.photos.sort(() => 0.5 - Math.random());
      return shuffled.slice(0, 5).map(photo => photo.src.large);
    }
    return [];
  } catch (error) {
    console.error('Error fetching weather images:', error);
    return [];
  }
};

